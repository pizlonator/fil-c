#include <stdfil.h>

typedef unsigned char v16qi __attribute__((vector_size(16)));

int main(void)
{
    v16qi a = {
        0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07,
        0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f
    };
    v16qi b = {
        0x10, 0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17,
        0x18, 0x19, 0x1a, 0x1b, 0x1c, 0x1d, 0x1e, 0x1f
    };

    asm volatile("aesdec %1, %0" : "+x"(a) : "x"(b));

    ZASSERT(a[0] == 0x03);
    ZASSERT(a[1] == 0xb2);
    ZASSERT(a[2] == 0x84);
    ZASSERT(a[3] == 0x0f);
    ZASSERT(a[4] == 0x3a);
    ZASSERT(a[5] == 0x0c);
    ZASSERT(a[6] == 0x3f);
    ZASSERT(a[7] == 0x79);
    ZASSERT(a[8] == 0x0f);
    ZASSERT(a[9] == 0x56);
    ZASSERT(a[10] == 0xe8);
    ZASSERT(a[11] == 0xa9);
    ZASSERT(a[12] == 0xda);
    ZASSERT(a[13] == 0xa0);
    ZASSERT(a[14] == 0x85);
    ZASSERT(a[15] == 0x4e);

    return 0;
}
