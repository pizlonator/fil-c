#include <stdfil.h>

typedef char v16qi __attribute__((vector_size(16)));

int main(void)
{
    v16qi a = { 0x12, 0x34, 0x56, 0x78, 0x9a, 0xbc, 0xde, 0xf0,
                0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77, 0x88 };
    v16qi b = { 0xff, 0x0f, 0xf0, 0x00, 0x55, 0xaa, 0xcc, 0x33,
                0xff, 0xff, 0x00, 0x00, 0x55, 0x55, 0xaa, 0xaa };
    v16qi r = a;
    asm volatile("pxor %1, %0" : "+x"(r) : "x"(b));
    for (int i = 0; i < 16; i++)
        ZASSERT((unsigned char)r[i] == (unsigned char)(a[i] ^ b[i]));
    return 0;
}
