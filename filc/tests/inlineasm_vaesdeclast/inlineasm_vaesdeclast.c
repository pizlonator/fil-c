#include <stdfil.h>
#include <wmmintrin.h>

__attribute__((target("aes"))) int main(void)
{
    __m128i a = _mm_setr_epi8(
        0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07,
        0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f);
    __m128i b = _mm_setr_epi8(
        0x10, 0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17,
        0x18, 0x19, 0x1a, 0x1b, 0x1c, 0x1d, 0x1e, 0x1f);
    __m128i expected = _mm_aesdeclast_si128(a, b);

    asm volatile("vaesdeclast %1, %0, %0" : "+x"(a) : "x"(b));

    int mask = _mm_movemask_epi8(_mm_cmpeq_epi8(a, expected));
    ZASSERT(mask == 0xffff);

    return 0;
}
