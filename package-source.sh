set -e
set -x

dir=$1
name=$2

test -n "$dir"
test -n "$name"

cd $dir
if git diff-index --quiet HEAD -- .
then
    git archive --format=tar.gz HEAD --prefix=$name/ -o $name.tar.gz
else
    rm -rf $name
    git archive --format=tar HEAD --prefix=$name/ | tar -xf -
    git diff --relative HEAD . | (cd $name && patch -p1)
    tar -czf $name.tar.gz $name
    rm -rf $name
fi

